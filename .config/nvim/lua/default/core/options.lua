local opt = vim.opt

-- numbers
opt.relativenumber = true
opt.number = true

-- tabs & indentation
opt.tabstop = 2
opt.shiftwidth = 2
opt.expandtab = true
opt.autoindent = true

-- search settings
opt.ignorecase = true
opt.smartcase = true

-- miscellaneous
opt.wrap = false
opt.cursorline = true

-- appearance 



-- backspace
opt.backspace = "indent,eol,start"

-- clipboard



-- split windows
opt.splitright = true -- split vertical window to the right
opt.splitbelow = true -- split horizontal window to the bottom

-- turn off swapfile
opt.swapfile = false

